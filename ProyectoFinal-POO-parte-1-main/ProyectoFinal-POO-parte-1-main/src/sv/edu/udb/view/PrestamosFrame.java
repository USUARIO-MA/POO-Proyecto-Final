    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
         try {
        Prestamo p = new Prestamo();

        // Leer valores desde los JTextField
        int idAlumno = Integer.parseInt(txtidAlumno.getText().trim());
        int idMaterial = Integer.parseInt(txtidMaterial.getText().trim());

        // Convertir fecha desde JTextField a java.util.Date
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date fechaPrestamo = sdf.parse(txtFechaPrestamo.getText().trim());
        Date fechaDevolucion = null;
        String fechaDevText = txtFechaDevolucion.getText().trim();
            if (!fechaDevText.isEmpty()) {
            fechaDevolucion = sdf.parse(fechaDevText);
         }


        String estado = cmbEstado.getSelectedItem().toString();

        // Llenar el objeto Prestamo
        p.setIdAlumno(idAlumno);
        p.setIdMaterial(idMaterial);
        p.setFechaPrestamo(fechaPrestamo);
        p.setFechaDevolucion(fechaDevolucion);
        p.setEstado(estado);

        // Llamar al DAO
        PrestamosDAO dao = new PrestamosDAO();
        boolean registrado = dao.registrarPrestamo(p);

        if (registrado) {
            log.info("Prestamo REGISTRADO CON EXITO");
            JOptionPane.showMessageDialog(this, "Préstamo registrado correctamente.");
            ListarPrestamos(); // Refrescar tabla
        } else {
            log.error("No se puedo regiustar el prestamo");
            JOptionPane.showMessageDialog(this, "No se pudo registrar el préstamo.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (NumberFormatException e) {
        log.error("Error ID de alumno o meterial invalido"+e.getMessage());
        JOptionPane.showMessageDialog(this, "ID de alumno o material inválido.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (ParseException e) {
        log.error("Error fecha de formato invalida Use yyyy-MM-dd."+e.getMessage());
        JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Use yyyy-MM-dd.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        log.error("Error Inesperado"+e.getMessage());
        JOptionPane.showMessageDialog(this, "Error inesperado: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }   
    }//GEN-LAST:event_btnRegistrarActionPerformed

    
    
    private void btnDevolucionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolucionActionPerformed
       try {
        // Validar que hay un ID de préstamo seleccionado
        if (txtidPrestamo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione un préstamo para modificar.");
           
            return;
        }

        // Crear objeto Prestamo con los datos de los campos
        Prestamo p = new Prestamo();

        p.setIdPrestamo(Integer.parseInt(txtidPrestamo.getText()));
        p.setIdAlumno(Integer.parseInt(txtidAlumno.getText()));
        p.setIdMaterial(Integer.parseInt(txtidMaterial.getText()));

        // Parsear fecha de préstamo
        Date fechaPrestamo = new SimpleDateFormat("yyyy-MM-dd").parse(txtFechaPrestamo.getText());
        p.setFechaPrestamo(fechaPrestamo);

        // Validar si la fecha de devolución está vacía
        if (txtFechaDevolucion.getText().trim().isEmpty()) {
            p.setFechaDevolucion(null);
        } else {
            Date fechaDevolucion = new SimpleDateFormat("yyyy-MM-dd").parse(txtFechaDevolucion.getText());
            p.setFechaDevolucion(fechaDevolucion);
        }

        p.setEstado(cmbEstado.getSelectedItem().toString());

        // Llamar al DAO
        PrestamosDAO dao = new PrestamosDAO();
        boolean resultado = dao.modificarPrestamo(p);

        if (resultado) {
            log.info("Préstamo modificado exitosamente");
            JOptionPane.showMessageDialog(this, "Préstamo modificado exitosamente.");
            ListarPrestamos();
            limpiarCampos();
        } else {
            log.error("No se puedo modificar el prestamo");
            JOptionPane.showMessageDialog(this, "No se pudo modificar el préstamo.");
        }

    } catch (Exception e) {
        log.error("Error: "+e.getMessage());
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnDevolucionActionPerformed

    
    
    private void tblPrestamosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPrestamosMouseClicked
      int fila = tblPrestamos.rowAtPoint(evt.getPoint());

    // Asegúrate que se haya seleccionado una fila válida
    if (fila >= 0) {
       txtidPrestamo.setText(tblPrestamos.getValueAt(fila, 0).toString());
        txtidAlumno.setText(tblPrestamos.getValueAt(fila, 1).toString());
        txtidMaterial.setText(tblPrestamos.getValueAt(fila, 3).toString());
        txtFechaPrestamo.setText(tblPrestamos.getValueAt(fila, 5).toString());

        // Validar que la fechaDevolución no esté vacía
        Object fechaDev = tblPrestamos.getValueAt(fila, 6);
        if (fechaDev != null) {
            txtFechaDevolucion.setText(fechaDev.toString());
        } else {
            txtFechaDevolucion.setText(""); // o "---"
        }

        cmbEstado.setSelectedItem(tblPrestamos.getValueAt(fila, 5).toString());
    }
    }//GEN-LAST:event_tblPrestamosMouseClicked

    
    
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Validar que se haya seleccionado un préstamo
    if (txtidPrestamo.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Seleccione un préstamo de la tabla para eliminar.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar este préstamo?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            int idPrestamo = Integer.parseInt(txtidPrestamo.getText());
            PrestamosDAO dao = new PrestamosDAO();
            boolean resultado = dao.eliminarPrestamo(idPrestamo);

            if (resultado) {
                log.info("Prestamo elimidado con exito");
                JOptionPane.showMessageDialog(this, "Préstamo eliminado exitosamente.");
                ListarPrestamos();
                limpiarCampos();
            } else {
                log.error("No se pudo eliminar el préstamo");
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el préstamo.");
            }
        } catch (NumberFormatException e) {
            log.error("ID de préstamo inválido");
            JOptionPane.showMessageDialog(this, "ID de préstamo inválido.");
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

