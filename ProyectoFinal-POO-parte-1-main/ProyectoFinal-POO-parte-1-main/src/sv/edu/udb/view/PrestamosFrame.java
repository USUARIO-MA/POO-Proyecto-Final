   private void ListarPrestamos() {
    DefaultTableModel model = new DefaultTableModel(
        new String[]{"ID Prestamo","ID Alumno","Nombre Alumno","ID Material","Titulo Material","Fecha Prestamo","Fecha Devolucion","Estado"},0
    ){
       @Override
       public boolean isCellEditable(int row, int column){
            return false;
            }
        };

    PrestamosDAO dao = new PrestamosDAO();
    List<Prestamo> lista = dao.ListarPrestamos();

    for (Prestamo p : lista) {
        model.addRow(new Object[]{
            p.getIdPrestamo(),
            p.getIdAlumno(),
            p.getNombreAlumno(),
            p.getIdMaterial(),
            p.getNombreMaterial(),
            p.getFechaPrestamo(),
            p.getFechaDevolucion(),
            p.getEstado()
        });
    }
    log.info("Prestamos listados con exito");
    tblPrestamos.setModel(model);
}

